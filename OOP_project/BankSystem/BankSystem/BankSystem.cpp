#include "BankSystem.h"


int main(void)
{
	int choice = 0;
	while (1)
	{
		ShowMenu();
		std::cout << "선택: ";
		std::cin >> choice;
		std::cout << std::endl;

		switch (choice)
		{
		case MAKE:
			MakeAccount();
			break;
		case DEPOSIT:
			DepositMoney();
			break;
		case WITHDRAW:
			WithdrawMoney();
			break;
		case INQUIRE:
			ShowAllAccinfo();
			break;
		case EXIT:
			return 0;
		default:
			std::cout << "Illegal selection.." << std::endl;
		}
	}
	return 0;
}

void ShowMenu(void)
{
	std::cout << "----------------Menu------------------" << std::endl;
	std::cout << "1. 계좌개설" << std::endl;
	std::cout << "2. 입  금" << std::endl;
	std::cout << "3. 출  금" << std::endl;
	std::cout << "4. 계좌정보 전체 출력" << std::endl;
	std::cout << "5. 프로그램 종료" << std::endl;
}

void MakeAccount(void)
{
	int id;
	char name[NAME_LEN];
	int balance;

	std::cout << "[계좌개설]" << std::endl;
	std::cout << "계좌ID: "; std::cin >> id;
	std::cout << "이름: "; std::cin >> name;
	std::cout << "입금액: "; std::cin >> balance;

	accArr[accNum].accID = id;
	accArr[accNum].balance = balance;
	strcpy(accArr[accNum].cusName, name);
	accNum++;
}

void DepositMoney(void)
{
	int id;
	int money;
	std::cout << "[입  금]" << std::endl;
	std::cout << "계좌ID: "; std::cin >> id;
	std::cout << "입금액: "; std::cin >> money;

	for (int i = 0; i < accNum; i++)
	{
		if (accArr[i].accID == id)
		{
			accArr[i].balance += money;
			std::cout << "입금완료" << std::endl << std::endl;
			return;
		}
	}
	std::cout << "전산상에 없는 계좌 입니다" << std::endl;
}
void WithdrawMoney(void)
{
	int id;
	int money;
	std::cout << "[출  금]" << std::endl;
	std::cout << "계좌ID: "; std::cin >> id;
	std::cout << "출금액: "; std::cin >> money;
	
	for (int i = 0; i < accNum; i++)
	{
		if (accArr[i].accID == id)
		{
			if (accArr[i].balance < money)
			{
				std::cout << "잔액부족" << std::endl << std::endl;
				return;
			}
			accArr[i].balance -= money;
			std::cout << "출금완료" << std::endl;
		}
	}
	std::cout << "전산상에 없는 계좌 입니다." << std::endl;
}
void ShowAllAccinfo(void)
{
	for (int i = 0; i < accNum; i++)
	{
		std::cout << "계좌ID: " << accArr[i].accID << std::endl;
		std::cout << "이 름: " << accArr[i].cusName << std::endl;
		std::cout << "잔 액: " << accArr[i].balance << std::endl << std::endl;
	}
}